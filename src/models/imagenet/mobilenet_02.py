		x = self.bn25(x)
		x = self.relu(x)
		x = self.conv26(x)
		x = self.bn26(x)
		x = self.relu(x)
		x = self.conv27(x)
		x = self.bn27(x)
		x = self.relu(x)
		x = self.avgpool(x)
		x = x.view(-1, x.size(1))
		x = self.fc(x)
		return x
