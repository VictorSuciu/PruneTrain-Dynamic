		self.bn22 = nn.BatchNorm2d(261)
		self.conv23 = nn.Conv2d(261, 291, kernel_size=1, stride=1, padding=0, bias=False)
		self.bn23 = nn.BatchNorm2d(291)
		self.conv24 = nn.Conv2d(291, 291, kernel_size=3, stride=2, padding=1, bias=False, groups=291)
		self.bn24 = nn.BatchNorm2d(291)
		self.conv25 = nn.Conv2d(291, 299, kernel_size=1, stride=1, padding=0, bias=False)
		self.bn25 = nn.BatchNorm2d(299)
		self.conv26 = nn.Conv2d(299, 299, kernel_size=3, stride=1, padding=1, bias=False, groups=299)
		self.bn26 = nn.BatchNorm2d(299)
		self.conv27 = nn.Conv2d(299, 418, kernel_size=1, stride=1, padding=0, bias=False)
		self.bn27 = nn.BatchNorm2d(418)
		self.avgpool = nn.AvgPool2d(7)
		self.relu = nn.ReLU(inplace=True)
		self.fc = nn.Linear(418, 1000)
